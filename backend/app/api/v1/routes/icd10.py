@router.get("/search")
async def search_icd10(
    q: str = Query("", min_length=0),
    limit: int = Query(20, ge=1, le=100),
):
    """Search ICD-10 codes by code or title."""
    if not q.strip():
        return ICD10_CATALOG[:limit]

    query = q.lower().strip()
    results = []
    # Exact code prefix match first
    for item in ICD10_CATALOG:
        if item["code"].lower().startswith(query):
            results.append(item)
    # Then title match
    for item in ICD10_CATALOG:
        if item not in results and query in item["title"].lower():
            results.append(item)

    return results[:limit]
